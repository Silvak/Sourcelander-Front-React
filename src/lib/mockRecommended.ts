export const generateExtendedRecommended = (): UnifiedFreelancer[] => {
  const targetCount = 25;
  const extendedList: UnifiedFreelancer[] = [];

  for (let i = 0; i < targetCount; i++) {
    const originalFreelancer =
      mockRecommendedFreelancers[i % mockRecommendedFreelancers.length];
    const duplicateNumber =
      Math.floor(i / mockRecommendedFreelancers.length) + 1;

    // Crear una copia con ID Ãºnico para evitar problemas de keys en React
    const duplicatedFreelancer: UnifiedFreelancer = {
      ...originalFreelancer,
      id: `${originalFreelancer.id}-dup-${duplicateNumber}`,
    };

    extendedList.push(duplicatedFreelancer);
  }

  return extendedList;
};
